	static class EchoServer extends Thread {

		private boolean running;
		private byte[] buf = new byte[65535];
		private String str;
		private int ctr = 0;
		private String[][] Sresults = new String[100][2];
		private String msg = new String("");

		public EchoServer() throws SocketException {
			int PORT = Integer.parseInt(input.getText());
			socket = new DatagramSocket(PORT);
		}

		public void run() {
			running = true;

			txtout.setFont(new Font("Dialog", Font.BOLD, 9));
			txtout.setForeground(new Color(0, 255, 0));
			txtout.append("SERVER : Port is open\nSERVER : listening....");

			while (running) {
				DatagramPacket packet = new DatagramPacket(buf, buf.length);
				try {
					socket.receive(packet);

					str = new String(packet.getData(), 0, packet.getLength());
					System.out.println("\nRecieved " + str);

				} catch (IOException e) {
					// TODO Auto-generated catch block
					e.printStackTrace();
				}

				// Do stuff to the received str plus prepare statements

				// split username and searched word

				String[] results = str.split("\\s*,\\s*");

				System.out.println("\nwe got " + results[0] + " searche for  " + results[1]);

				txtout.setFont(new Font("Dialog", Font.BOLD, 9));
				txtout.setForeground(new Color(0, 255, 0));
				txtout.append("\nRecieved " + str + "\nProcessing....");

				// find number of occurances of a word

				String[] words = text.split(" ");
				String word = results[1];
				int occ = 0;
				for (int i = 0; i < words.length; i++) {
					if (words[i].equals(word)) {
						occ++;
					}
				}
				System.out.println("\nnumber of occurences = " + occ);

				msg = ("Number of occurences for the word : '" + word + "' is = " + occ + " times.\n\n");

				// add user and the searched for word

				Sresults[ctr][0] = results[0];
				Sresults[ctr][1] = results[1];

				System.out.println(Sresults[ctr][0] + " searched for " + Sresults[ctr][1]);

				// query db for all users who searched for the same word

				for (int i = 0; i <= ctr; i++) {

					if (Sresults[i][1].equals(results[1])) {

						msg = msg + ("Client " + Sresults[i][0] + " , Searched For : " + Sresults[i][1] + "\n");
					}
				}

				System.out.println(msg);
				buf = msg.getBytes();
				InetAddress address = packet.getAddress();
				int port = packet.getPort();
				packet = new DatagramPacket(buf, buf.length, address, port);

				ctr++;

				try {
					socket.send(packet);
				} catch (IOException e) {
					// TODO Auto-generated catch block
					e.printStackTrace();
				}
			}
			socket.close();
		}
	}

